public class ContactInfo {
    public String odds;
    public String even;
    public String name;
    public String lastName;
    public String description;

    public ContactInfo(String odds,String even,String name, String lastName, String description){
        this.odds = odds;
        this.even = even;
        this.name = name;
        this.lastName = lastName;
        this.description = description;
    }


}